// ListAgents lists agents with filtering and pagination
func (ac *AgentClient) ListAgents(ctx context.Context, tenantID string, page, pageSize int, status string) (*contracts.ListAgentsResponse, error) {
	endpoint := fmt.Sprintf("%s/api/v1/tenants/%s/agents", ac.baseURL, tenantID)

	// Add query parameters
	params := url.Values{}
	if page > 0 {
		params.Add("page", strconv.Itoa(page))
	}
	if pageSize > 0 {
		params.Add("page_size", strconv.Itoa(pageSize))
	}
	if status != "" {
		params.Add("status", status)
	}

	if len(params) > 0 {
		endpoint += "?" + params.Encode()
	}

	httpReq, err := http.NewRequestWithContext(ctx, "GET", endpoint, nil)
	if err != nil {
		return nil, fmt.Errorf("failed to create request: %w", err)
	}

	resp, err := ac.httpClient.Do(httpReq)
	if err != nil {
		return nil, fmt.Errorf("failed to make request: %w", err)
	}
	defer resp.Body.Close()

	body, err := io.ReadAll(resp.Body)
	if err != nil {
		return nil, fmt.Errorf("failed to read response: %w", err)
	}

	if resp.StatusCode != http.StatusOK {
		return nil, fmt.Errorf("request failed with status %d: %s", resp.StatusCode, string(body))
	}

	var response contracts.ListAgentsResponse
	if err := json.Unmarshal(body, &response); err != nil {
		return nil, fmt.Errorf("failed to unmarshal response: %w", err)
	}

	return &response, nil
}

